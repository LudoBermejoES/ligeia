/// Lookup function for keyword folders
pub fn lookup_keyword_folders(keyword_tag: &str) -> Option<&'static [&'static str]> {
    KEYWORD_FOLDER_MAPPINGS.iter()
        .find(|(tag, _)| *tag == keyword_tag)
        .map(|(_, folders)| *folders)
}

/// Get all keyword tags
pub fn get_all_keyword_tags() -> Vec<&'static str> {
    KEYWORD_FOLDER_MAPPINGS.iter()
        .map(|(tag, _)| *tag)
        .collect()
}

/// Build lookup HashMap for efficient access
pub fn build_keyword_lookup() -> HashMap<&'static str, &'static [&'static str]> {
    KEYWORD_FOLDER_MAPPINGS.iter().cloned().collect()
}